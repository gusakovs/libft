#include "../inc/libft.h"

int	ft_putstr(char *s)
{
	int	n;

	n = 0;
	if (!s)
	{
		s = "\0";
		n = 1;
	}
	while (*s)
		n += ft_putchar(*s++);
	return (n);
}
